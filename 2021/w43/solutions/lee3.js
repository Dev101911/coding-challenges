const hasItAll = (n) => {
  let m = n.toLowerCase();
  if (m.indexOf("a") === -1) return false;
  if (m.indexOf("b") === -1) return false;
  if (m.indexOf("c") === -1) return false;
  if (m.indexOf("d") === -1) return false;
  if (m.indexOf("e") === -1) return false;
  if (m.indexOf("f") === -1) return false;
  if (m.indexOf("g") === -1) return false;
  if (m.indexOf("h") === -1) return false;
  if (m.indexOf("i") === -1) return false;
  if (m.indexOf("j") === -1) return false;
  if (m.indexOf("k") === -1) return false;
  if (m.indexOf("l") === -1) return false;
  if (m.indexOf("m") === -1) return false;
  if (m.indexOf("n") === -1) return false;
  if (m.indexOf("o") === -1) return false;
  if (m.indexOf("p") === -1) return false;
  if (m.indexOf("q") === -1) return false;
  if (m.indexOf("r") === -1) return false;
  if (m.indexOf("s") === -1) return false;
  if (m.indexOf("t") === -1) return false;
  if (m.indexOf("u") === -1) return false;
  if (m.indexOf("v") === -1) return false;
  if (m.indexOf("w") === -1) return false;
  if (m.indexOf("x") === -1) return false;
  if (m.indexOf("y") === -1) return false;
  if (m.indexOf("z") === -1) return false;

  return true;
};

module.exports = hasItAll;
